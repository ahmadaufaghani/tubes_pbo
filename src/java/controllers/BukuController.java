    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String menu = request.getParameter("menu");
        String key = request.getParameter("key");
        
        Buku bk = new Buku();
        if(request.getParameterMap().isEmpty()) {
            ArrayList<Buku> bk1 = bk.get();
            request.setAttribute("list",bk1);
            request.getRequestDispatcher("book.jsp").forward(request, response);           
        } else if (key!=null) {
            ArrayList<Buku> bk2 = bk.find(key);
            request.setAttribute("list",bk2);
            request.setAttribute("content-title","Search Result");
            request.setAttribute("content-desc","Key : "+key);
            request.setAttribute("key", key);
            request.getRequestDispatcher("book_search.jsp").forward(request, response);    
        }
    }

